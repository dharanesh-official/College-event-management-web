document.addEventListener("DOMContentLoaded", () => {
  const modal = document.getElementById("event-modal");
  const modalTitle = document.getElementById("modal-title");
  const modalBody = document.getElementById("modal-body");
  const closeButton = document.getElementById("modal-close");

  // Description button logic
  document.querySelectorAll('[data-event]').forEach(button => {
    if (button.textContent.trim() === "Description") {
      button.addEventListener("click", () => {
        const key = button.getAttribute("data-event");
        const data = eventDescriptions[key] || eventDescriptions["Present-O-Vation"];
        modalTitle.textContent = data.title + " Description";
        modalBody.innerHTML = data.html;
        modal.classList.remove("hidden");
      });
    } else if (button.textContent.trim() === "Rules") {
      button.addEventListener("click", () => {
        const key = button.closest("[data-event]")?.getAttribute("data-event") || button.parentElement.querySelector('[data-event]').getAttribute("data-event");
        const data = eventDescriptions[key] || eventDescriptions["Present-O-Vation"];
        modalTitle.textContent = data.title + " Rules";
        modalBody.innerHTML = data.rules || "<p>Rules not available for this event.</p>";
        modal.classList.remove("hidden");
      });
    }
  });

  closeButton.addEventListener("click", () => {
    modal.classList.add("hidden");
  });
  modal.addEventListener("click", e => {
    if (e.target === modal) modal.classList.add("hidden");
  });
});